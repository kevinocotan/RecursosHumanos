    private PermisosModel crearModelo() { 
        int c= Integer.parseInt(this.Codemp.getText());
        int t= Integer.parseInt(this.Tipo.getText());
        String f= this.Fecha.getText();
        String h= this.Hora.getText();
        String o= this.Observaciones.getText();
        PermisosModel pm =new PermisosModel(this.codpermiso,c,t,f,h,o);
        return pm;
    }
    
    private void limpiar() {
        this.Codemp.setText("");
        this.Fecha.setText("");
        this.Hora.setText("");
        this.Observaciones.setText("");
        this.Tipo.setText("");
        this.codpermiso=0;       
    }
    
    private void activarObjetos (boolean estado) {
        this.Codemp.setEditable(estado);
        this.Fecha.setEditable(estado);
        this.Hora.setEditable(estado);
        this.Observaciones.setEditable(estado);
        this.Tipo.setEnabled(!estado);
        this.Nuevo.setEnabled(!estado);
        this.Modificar.setEnabled(!estado);
        this.Eliminar.setEnabled(!estado);
        this.Filtrar.setEnabled(!estado);
        this.Guardar.setEnabled(estado);
        this.Cancelar.setEnabled(estado);              
    }
       
