    private void FiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FiltrarActionPerformed
        String codigo=JOptionPane.showInputDialog(this, "Digite el Codigo del Permiso");
        int cod=Integer.parseInt(codigo);
        PermisosModel obj = this.permisosDAO.getOne(cod);
        if (obj!=null) {
            this.codpermiso=obj.getCodpermiso();
            this.Codemp.setText(String.valueOf(obj.getCodemp()));
            this.Tipo.setText(String.valueOf(obj.getTipo()));
            this.Fecha.setText(obj.getFecha());
            this.Hora.setText(obj.getHora());
            this.Observaciones.setText(obj.getObservaciones());
        } else {
            JOptionPane.showMessageDialog(this, "El Codigo no existe");
        }
    }//GEN-LAST:event_FiltrarActionPerformed

